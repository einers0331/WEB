$(document).ready(function(){
$('#mostrar').click(function(){
    $("#color").show();
    $("#marca").show();
    $("#estilo").show();
    $("#modelo").show();
    $("#a").show();
    $("#b").show();
    $("#c").show();
    $("#d").show();
    $("#e").show();
    $("#range").show();

});
$('#ocultar').click(function(){
    $("#color").hide();
    $("#marca").hide();
    $("#estilo").hide();
    $("#modelo").hide();
    $("#a").hide();
    $("#b").hide();
    $("#c").hide();
    $("#d").hide();
    $("#e").hide();
    $("#range").hide();

});
});
